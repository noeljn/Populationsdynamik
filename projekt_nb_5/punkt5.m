function [u]=punkt5(u0)
    u = u0;
    h = 1;
    iter = 0;
    func = @(u)[15.*u(1)-1.7*10^(-5).*u(1).^2-0.022.*u(1).*u(2);
             -1.9.*u(2).^(1.4)+0.088.*u(1).^(0.6).*u(2).^(0.8)-1.4.*u(2).*u(3);
             -1.8.*u(3) + 0.028.*u(2).*sqrt(u(3))];
    jac = @(u)[15-3.4*10^(-5).*u(1)-0.022.*u(2),-0.022.*u(1),0;
             0.0528.*u(2).^(0.8).*u(1).^(-0.4),-2.66.*u(2).^(0.4)+0.0704.*u(1).^(0.6).*u(2).^(-0.2)-1.4*u(3),-1.4.*u(2);
             0,0.028.*sqrt(u(3)),-1.8 + 0.028.*u(2).*0.5.*1./sqrt(u(3))];
    %Stops until tolerance is reached or iteration is 1000
    while norm(h) > 10^(-6) && iter < 100
        iter = iter + 1;
        h=-jac(u)\func(u);
        u = u + h;
    end
end
